void v_init(void) {
    vga_init();
    vga_fill(C_BLUE);
    vga_rect(0, 200 - 10, 320, 10, C_LGREY);
}

void v_put(unsigned int r, unsigned int c, char ch, unsigned char fg, unsigned char bg) {
    if (ch < 32 || ch > 127) {
        ch = '?';
    }
    unsigned int char_idx = (unsigned int)ch - 32;
    unsigned int pixel_x = c * CHAR_WIDTH;
    unsigned int pixel_y = r * CHAR_HEIGHT;

    unsigned int row, col;
    for (row = 0; row < CHAR_HEIGHT; row++) {
        unsigned char line = font8x8_basic_data[char_idx][row];
        for (col = 0; col < CHAR_WIDTH; col++) {
            if ((line >> (7 - col)) & 0x1) {
                vga_pixel(pixel_x + col, pixel_y + row, fg);
            } else {
                vga_pixel(pixel_x + col, pixel_y + row, bg);
            }
        }
    }
}

void v_str(unsigned int r, unsigned int c, const char* str, unsigned char fg, unsigned char bg) {
    unsigned int current_c = c;
    while (*str != '\0') {
        v_put(r, current_c, *str, fg, bg);
        current_c++;
        str++;
    }
}

void v_fill(unsigned int r1, unsigned int c1, unsigned int r2, unsigned int c2, unsigned char fg, unsigned char bg) {
    unsigned int pixel_x1 = c1 * CHAR_WIDTH;
    unsigned int pixel_y1 = r1 * CHAR_HEIGHT;
    unsigned int pixel_x2 = (c2 + 1) * CHAR_WIDTH - 1;
    unsigned int pixel_y2 = (r2 + 1) * CHAR_HEIGHT - 1;

    if (pixel_x1 >= 320) pixel_x1 = 319;
    if (pixel_y1 >= 200) pixel_y1 = 199;
    if (pixel_x2 >= 320) pixel_x2 = 319;
    if (pixel_y2 >= 200) pixel_y2 = 199;

    unsigned int width = pixel_x2 - pixel_x1 + 1;
    unsigned int height = pixel_y2 - pixel_y1 + 1;

    vga_rect(pixel_x1, pixel_y1, width, height, bg);
}